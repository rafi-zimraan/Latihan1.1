// PUSH (menambah)
let car : any = ['toyota', 'mazda' , 'corolla', 'civic'];
car.push("GR supra");
console.log('push', car);

// POP ( Dari Belakang )
const food : any =['taco', 'stiek', 'soto' ,'bakwan'];
food.pop('')
